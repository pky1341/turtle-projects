import turtle

s=turtle.getscreen()
turtle.ht()
s.title("graph design")
t1=turtle.Turtle()
t1.speed(10)
#vertical line
s.bgcolor("black")
for i in range(0,400,20):
    t1.pencolor("lightgray")
    t1.penup()
    t1.setpos(-200+i,-200)
    if i==0:
        t1.lt(90)
    t1.pendown()
    t1.fd(400)
    t1.bk(400)
for i in range(0,400,20):
    t1.penup()
    t1.setpos(-200,200-i)
    if i==0:
        t1.rt(90)
    t1.pendown()
    t1.fd(400)
    t1.bk(400)

t1.penup()
t1.home()
t1.pendown()
t1.width(5)
t1.pencolor("blue")
t1.fd(200)
t1.bk(400)
t1.penup()
t1.home()
t1.lt(90)
t1.pendown()
t1.fd(200)
t1.bk(400)
t1.penup()
t1.goto(0,-230)
t1.write("Y`",font=("Arial",15,"bold"))
t1.goto(0,205)
t1.write("Y",font=("Arial",15,"bold"))
t1.goto(-220,0)
t1.write("X`",font=("Arial",15,"bold"))
t1.goto(205,0)
t1.write("X",font=("Arial",15,"bold"))
