import turtle

s=turtle.getscreen()
turtle.ht()
t1=turtle.Turtle()
s.bgcolor("black")
t1.pencolor("blue")
def curve():
    for i in range(200):
        t1.fd(1)
        t1.rt(1)

def shape():
    t1.fillcolor("red")
    t1.begin_fill()
    t1.lt(140)
    t1.fd(112)
    curve()
    t1.lt(120)
    curve()
    t1.fd(113)
    t1.end_fill()

shape()
